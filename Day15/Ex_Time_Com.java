// write a program on  Exponential time complexity
 public class Ex_Time_Com
 {
 public static void main(String args[])
 {
 int c=0;
 for(int i=1;i<=Math.pow(2,6);i++)
 {
 c++;
 System.out.println(i);
 }
 System.out.print(c);
 }
 }
