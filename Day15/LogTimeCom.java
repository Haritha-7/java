// write a programs on Logarithmic Time complexity.
public class LogTimeCom
{
public static void main(String args[])
{
int n=8;
for(int i=1;i<n;i+=3)
{
System.out.println(i);
}
}
}
// here, we give input size 8,but it does not run 8 times means it does not depends on input value .here, we are increasing i value 3 times  that's why it runs only 3 times.
