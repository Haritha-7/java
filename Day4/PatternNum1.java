//write a program in java to dispaly the pattern like right angle triangle with a number which will repeat a number in a row.
public class PatternNum1
{
public static void main(String args[])
{
int n=4;
for(int i=1;i<=n;i++)
{
for(int j=1;j<=i;j++)
{
System.out.print(i);
}
System.out.println("");
}
}
}
