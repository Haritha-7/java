public class AssignmentOperators
{
public static void main(String args[])
{
// it is used to assign the values to the variable
int age;
// =operator
System.out.println(age=12);//assigning the value
//+= operator  
System.out.println(age+=5);//adding and assigning value age+5=17
//-= operator  
System.out.println(age-=3);//subtracting and assigning value age-3=14
//*= operator
System.out.println(age*=4);//multiplying and assining value. age*4=56
// /=operator
System.out.println(age/=3);// diving & assining value.returns quotient. age/3=18
// %=operator
System.out.println(age%=5);// divides and assining value.returns remaindeers.age%5=3
}
}
