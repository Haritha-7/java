public class IncreementOperators
{
public static void main(String args[])
{
//it increasing one value to the original value
//post-increement
int a=10;
int b;
 b=a++;
System.out.println(a);
System.out.println(b);
//pre-increement
 b=++a;
System.out.println(a);
System.out.println(b);
//it decreesing the  one value to the original value
//post-decreement
 b=a--;
System.out.println(a);
System.out.println(b);
//pre-decreement
b=--a;
System.out.println(a);
System.out.println(b);
}
}
