public class ShiftingOperators
{
public static void main(String args[])
{
int x=25;
//left shifting .it double the given value
System.out.println(x<<3);
//right shifing.it decreesing half of the given value
System.out.println(x>>1);
//unsigned right shift operator.it moves the bits of the integer given number of places to the right.sign bit was filled with zero's.
System.out.println(x>>>1);
}
}
