public class ArthmeticOperators
{
public static void main(String args[])
{
int a=35;
int b=28;
//adding the  given numbers
System.out.println(a+b);
//subtracting the given numbers
System.out.println(a-b);
//multiplying the numbers
System.out.println(a*b);
//diving the two numbers and returns the quotient
System.out.println(a/b);
//divides the two numbers and returns the remainder
System.out.println(a%b);
}
}
