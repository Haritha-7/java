public class RelationalOperators
{
public static void main(String args[])
{
int a=7;
int b=11;
//it checks the condition whether true or false
//== operator
System.out.println(a==b);//false
//!= operators
System.out.println(a!=b);//true
//> operator
System.out.println(a>b);//false
//< operators
System.out.println(a<b);//true
//>= operator
System.out.println(a>=b);//false
//<=operator
System.out.println(a<=b);//true
}
}


