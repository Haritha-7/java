public class BitwiseOperators
{
public static void main(String args[])
{
int a=16;
int b=21;
//Bitwise AND operator
System.out.println(a&b);//returns bit by bit AND of input values
//Bitwise OR operator
System.out.println(a|b);//returns bit by bit  OR of input values
//Birwise XOR operators
System.out.println(a^b);//returns bit by bit XOR of input values
//Bitwise complement operator
System.out.println(~a);//returns one's complement representation of input value
}
}

